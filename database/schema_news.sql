-- ------------------------------------------------------------
--  news – church announcements, events, news articles
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS news (
  id            INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  title         VARCHAR(255)    NOT NULL COMMENT 'Заглавие на новината',
  summary       VARCHAR(500)    NULL     COMMENT 'Кратко резюме (за списъка)',
  body          TEXT            NOT NULL COMMENT 'Пълен текст на новината',
  image_url     VARCHAR(500)    NULL     COMMENT 'URL на изображение (по избор)',
  author        VARCHAR(255)    NULL     COMMENT 'Автор / свещенослужител',
  published_at  DATETIME        NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Дата на публикуване',
  is_pinned     TINYINT(1)      DEFAULT 0 COMMENT '1 = закачена отгоре',
  is_active     TINYINT(1)      DEFAULT 1 COMMENT '0 = скрита',
  created_at    DATETIME        DEFAULT CURRENT_TIMESTAMP,
  updated_at    DATETIME        DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

  INDEX idx_published (published_at),
  INDEX idx_active    (is_active),
  INDEX idx_pinned    (is_pinned)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

